public class modelo {

    
    public int Cuadrado(int x, int y)
    {
        return x * y;
    }
    
    public int rectangulo (int x, int y)
    {
        return x * y;
    }
    
    public int triangulo(int x, int y )
    {
      
        
         return (x*y)/2; 
    }
    
    public double circulo (double  x)
    {
          return (x*x)*3.1416 ;
    }
}


